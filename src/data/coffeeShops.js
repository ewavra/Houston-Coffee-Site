// Parse a score value that may be "4.5 - Some description" or "Yes" / "No" / "Maybe"
function parseScore(val) {
  if (val === null || val === undefined || val === '') return null;
  const s = String(val).trim();
  if (s === '0') return 0;
  const numMatch = s.match(/^(\d+\.?\d*)/);
  if (numMatch) return parseFloat(numMatch[1]);
  const lower = s.toLowerCase();
  if (lower.startsWith('yes - limited')) return 3;
  if (lower.startsWith('yes')) return 5;
  if (lower.startsWith('maybe')) return 2.5;
  if (lower.startsWith('no')) return 0;
  return null;
}

